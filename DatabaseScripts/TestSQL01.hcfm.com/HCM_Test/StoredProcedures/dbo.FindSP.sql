create PROC [dbo].[FindSP]
	@SearchCrit VARCHAR(1000)
AS

SELECT ROUTINE_NAME
FROM INFORMATION_SCHEMA.ROUTINES
WHERE ROUTINE_DEFINITION LIKE '%' + @SearchCrit + '%'
ORDER BY ROUTINE_NAME
GO
